package com.Ejercicio.Jueves;

public class Salones {
	
	private int numSalon;
	private String letra;
	public Salones(int numSalon, String letra) {
		this.numSalon = numSalon;
		this.letra = letra;
	}
	@Override
	public String toString() {
		return "Salones [numSalon=" + numSalon + ", letra=" + letra + "]";
	}
	
	
	
	

}
